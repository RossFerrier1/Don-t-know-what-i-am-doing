    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if(tblOrderLines.getSelectedRow() == -1)
        {
            lblMessage.setText("Select Clothing First");
        }
        else
        {
            DefaultTableModel model = (DefaultTableModel)tblOrderLines.getModel();
            int ProductId =
            Integer.parseInt(String.valueOf(model.getValueAt(tblOrderLines.getSelectedRow(), 0)));

            loggedInCustomer.findLatestOrder().removeOrderLine(ProductId);

            model.removeRow(tblOrderLines.getSelectedRow());

            lblMessage.setText("Clothing Has Been Removed");
            lblTotalCost.setText("Â£" + String.format("%.02f",loggedInCustomer.findLatestOrder().getOrderTotal()));
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderActionPerformed

        DBManager db = new DBManager();

        for(Map.Entry<Integer, OrderLine> olEntry : loggedInCustomer.findLatestOrder().getOrderLines().entrySet())
        {
            OrderLine actualOrderLine = olEntry.getValue();
           Product orderedClothing = actualOrderLine.getProduct();

            orderedClothing.setIsAvailable(false);
            db.updateClothingAvailability(orderedClothing);
        }

        loggedInCustomer.findLatestOrder().setStatus("Complete");
        db.completeOrder(orderId);

        Confirmation confirmation = new Confirmation(loggedInCustomer, orderId);
        this.dispose();
        confirmation.setVisible(true);
    }//GEN-LAST:event_btnOrderActionPerformed


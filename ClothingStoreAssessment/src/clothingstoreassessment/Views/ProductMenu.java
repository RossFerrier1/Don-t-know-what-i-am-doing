    private void lstCategoryValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstCategoryValueChanged

        DefaultListModel model = new DefaultListModel();
        for(Map.Entry<Integer, Product> animalEntry : products.entrySet())
        {
            Product actualProduct = animalEntry.getValue();
            if(actualProduct.getClass().getName().equals("models." + lstCategory.getSelectedValue())
                && actualProduct.getIsAvailable())
            {
                model.addElement(actualProduct);
            }
        }
        lstProductType.setModel(model);
        lblMessage.setText("");
    }//GEN-LAST:event_lstCategoryValueChanged

    private void lstProductTypeValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstProductTypeValueChanged
        Object animalObject = lstProductType.getSelectedValue();
        selectedProduct = (Product)animalObject;
        lblMessage.setText("");
    }//GEN-LAST:event_lstProductTypeValueChanged

    private void btnEditProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditProductActionPerformed
        int selection = lstProductType.getSelectedIndex();

        if(selection == -1)
        {
            lblMessage.setText("Select Product First");
        }
        else
        {
            EditProduct editProduct = new EditProduct(loggedInStaff, selectedProduct);
            this.dispose();
            editProduct.setVisible(true);
        }
    }//GEN-LAST:event_btnEditProductActionPerformed


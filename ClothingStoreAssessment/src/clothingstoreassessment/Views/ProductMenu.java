    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int selection = lstProductType.getSelectedIndex();

        if(selection == -1)
        {
            lblMessage.setText("Select Product First");
        }
        else
        {
            DBManager db = new DBManager();
            db.deleteProduct(selectedProduct);
            products.remove(selectedProduct.getProductId());

            DefaultListModel model = (DefaultListModel)lstProductType.getModel();
            model.remove(selection);
            lblMessage.setText("Product Removed Successfully");
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

